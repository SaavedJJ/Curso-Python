###
# 01 - type()
# tipos de datos de python
# int, float, complex, str, bool, NoneType, list, tuple, dict, range, set ...
###

print("int:")
print(type(10))
print(0)
print(-5)
print(53453453535)

print("float:")
print(type(3.14))
print(type(1.0))
print(1e2)

print("complex:")
print(type(1 + 2j))
print(1 + 2j)

print("str:")
print(type("hola"))
print(type(""))
print(type("123"))
print(type("""
           
           hola

"""))

print("bool:")
print(type(False))
print(type(True))
print(type(1 < 2))

print("NoneType:")
print(type(None))



